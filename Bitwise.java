public class Bitwise {
    public static void main(String[] args) {
        //Bitwise shift Operators
        //left shift(<<)
        //signed right shift(>>)
        //unsigned right shift(>>>)
        System.out.println(4 << 1);
        System.out.println(4 >> 1);
        System.out.println(4 >>> 1);
        System.out.println(-4 << 1);
        System.out.println(-4 >> 1);
        System.out.println(-4 >>> 1);
        System.out.println(Integer.MAX_VALUE);
    }
}
